package entity;


public class Xcscorerank {

  private String commentPlace;
  private double viewRank;
  private double priceRank;
  private double interestRank;
  private double totalRank;


  public String getCommentPlace() {
    return commentPlace;
  }

  public void setCommentPlace(String commentPlace) {
    this.commentPlace = commentPlace;
  }


  public double getViewRank() {
    return viewRank;
  }

  public void setViewRank(double viewRank) {
    this.viewRank = viewRank;
  }


  public double getPriceRank() {
    return priceRank;
  }

  public void setPriceRank(double priceRank) {
    this.priceRank = priceRank;
  }


  public double getInterestRank() {
    return interestRank;
  }

  public void setInterestRank(double interestRank) {
    this.interestRank = interestRank;
  }


  public double getTotalRank() {
    return totalRank;
  }

  public void setTotalRank(double totalRank) {
    this.totalRank = totalRank;
  }

}
